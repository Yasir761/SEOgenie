export interface TwitterTokenData {
  accessToken: string;            // OAuth 1.0a token
  accessSecret: string;           // OAuth 1.0a secret
  userId: string;                 // Twitter user ID (numeric)
  screenName: string;            // Twitter handle (e.g. "@username")
  expiresAt?: number;            // Optional: Epoch timestamp of token expiry
  tokenType?: string;            // Optional: Token type (if you later support OAuth 2.0)
  createdAt?: number;            // Optional: When token was first issued
  scopes?: string[];             // Optional: Future use for OAuth 2.0 scopes
}
